def _card(title: str, rows: list[tuple[str, object]]) -> str:
    items = "".join(
        f"<div class=\"row\"><dt>{html.escape(str(label))}</dt><dd>{html.escape(str(value))}</dd></div>"
        for label, value in rows
    )
    return f"<section class=\"panel\"><h2>{html.escape(title)}</h2><dl>{items}</dl></section>"


def _future_card(status: ControlCenterStatus) -> str:
    items = "".join(
        f"<li>{html.escape(str(item.get('name', 'Future module')))}: {html.escape(str(item.get('status', 'locked/disabled')))}</li>"
        for item in status.future_modules
    )
    return f"<section class=\"panel\"><h2>Future Locked Modules</h2><ul>{items}</ul></section>"


def _value(source: dict[str, object], key: str) -> str:
    value = source.get(key, "Unavailable")
    if isinstance(value, bool):
        return "yes" if value else "no"
    return str(value)


def _yes_no(value: object) -> str:
    if isinstance(value, bool):
        return "yes" if value else "no"
    return str(value if value is not None else "Unavailable")


def _sum_values(source: dict[str, object], *keys: str) -> int:
    total = 0
    for key in keys:
        try:
            total += int(source.get(key, 0) or 0)
        except (TypeError, ValueError):
            continue
    return total


